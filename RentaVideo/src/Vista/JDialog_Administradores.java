        public void llenadoDeTabla() {
        DefaultTableModel tabla = new DefaultTableModel();
        tabla.addColumn("ID");
        tabla.addColumn("Nombre");
        tabla.addColumn("Apellido");
        tabla.addColumn("Direccion");
        tabla.addColumn("Telefono");
        tabla.addColumn("Correo");
        tabla.addColumn("Estado");
        tabla.addColumn("Usuario");

        ClsAdministradores admin = new ClsAdministradores();

        List<ClsAdministradores> ListadoAdministradores = admin.getListadoAdministradores();
        Tbc_Admin.setModel(tabla);
        String[] dato = new String[9];
        for (int i = 0; i < ListadoAdministradores.size(); i++) {
            dato[0] = Integer.toString(ListadoAdministradores.get(i).getIdAdmin());
            dato[1] = ListadoAdministradores.get(i).getNombreAdmin();
            dato[2] = ListadoAdministradores.get(i).getApellidoAdmin();
            dato[3] = ListadoAdministradores.get(i).getDireccionAdmin();
            dato[4] = ListadoAdministradores.get(i).getTelefonoAdmin();
            dato[5] = ListadoAdministradores.get(i).getCorreoAdmin();
            dato[6] = ListadoAdministradores.get(i).getEstadoAdmin();
            dato[7] = ListadoAdministradores.get(i).getNombreUsuario();
            tabla.addRow(dato);
        }       
    }
    
    
